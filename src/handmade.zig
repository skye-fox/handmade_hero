fn initializeArena(arena: *MemoryArena, size: usize, base: [*]u8) void {
    arena.size = size;
    arena.base = base;
    arena.used = 0;
}

fn pushStruct(arena: *MemoryArena, size: usize) [*]u8 {
    std.debug.assert(arena.used + size <= arena.size);
    const result = arena.base + arena.used;
    arena.used += size;
    return result;
}

pub fn pushArray(arena: *MemoryArena, count: u32, size: usize) [*]u8 {
    const array_size = count * size;
    std.debug.assert(arena.used + array_size <= arena.size);
    const result = arena.base + arena.used;
    arena.used += array_size;
    return result;
}

pub export fn gameUpdateAndRender(thread: *ThreadContext, memory: *GameMemory, input: *GameInput, buffer: *GameOffScreenBuffer) void {
    _ = thread;
    std.debug.assert((&input.controllers[0].button.input.terminator - &input.controllers[0].button.buttons[0]) == input.controllers[0].button.buttons.len);
    std.debug.assert(@sizeOf(GameState) <= memory.permanent_storage_size);

    const player_height = 1.4;
    const player_width = 0.75 * player_height;

    const game_state: *GameState = @ptrCast(@alignCast(memory.permanent_storage));
    if (!memory.is_initialized) {
        game_state.player_pos.abs_tile_x = 1;
        game_state.player_pos.abs_tile_y = 3;
        game_state.player_pos.offset_x = 5.0;
        game_state.player_pos.offset_y = 5.0;

        initializeArena(&game_state.world_arena, memory.permanent_storage_size - @sizeOf(GameState), @as([*]u8, @ptrCast(memory.permanent_storage)) + @sizeOf(GameState));

        game_state.world = @ptrCast(@alignCast(pushStruct(&game_state.world_arena, @sizeOf(World))));
        var world: *World = game_state.world;
        world.tile_map = @ptrCast(@alignCast(pushStruct(&game_state.world_arena, @sizeOf(tile.TileMap))));
        var tile_map: *tile.TileMap = world.tile_map;

        tile_map.chunk_shift = 4;
        tile_map.chunk_mask = (@as(u32, @intCast(1)) << @as(u5, @intCast(tile_map.chunk_shift))) - 1;
        tile_map.chunk_dim = (@as(u32, @intCast(1)) << @as(u5, @intCast(tile_map.chunk_shift)));

        tile_map.tile_chunk_count_x = 128;
        tile_map.tile_chunk_count_y = 128;
        tile_map.tile_chunk_count_z = 2;

        const chunk_count: u32 = tile_map.tile_chunk_count_x * tile_map.tile_chunk_count_y * tile_map.tile_chunk_count_z;
        tile_map.tile_chunks = @ptrCast(@alignCast(pushArray(&game_state.world_arena, chunk_count, @sizeOf(tile.TileChunk))));

        tile_map.tile_side_in_meters = 1.4;

        var random_number_index: u32 = 0;
        const tiles_per_width: u32 = 17;
        const tiles_per_height: u32 = 9;
        var screen_x: u32 = 0;
        var screen_y: u32 = 0;
        var abs_tile_z: u32 = 0;

        var door_left = false;
        var door_right = false;
        var door_top = false;
        var door_bottom = false;
        var door_up = false;
        var door_down = false;

        const num_screens: u32 = 100;
        for (0..num_screens) |_| {
            std.debug.assert(random_number_index < random_number_table.len);
            var random_choice: u32 = 0;
            if (door_up or door_down) {
                random_choice = random_number_table[random_number_index] % 2;
                random_number_index += 1;
            } else {
                random_choice = random_number_table[random_number_index] % 3;
                random_number_index += 1;
            }

            var created_z_door = false;
            if (random_choice == 2) {
                created_z_door = true;
                if (abs_tile_z == 0) {
                    door_up = true;
                } else {
                    door_down = true;
                }
            } else if (random_choice == 1) {
                door_right = true;
            } else {
                door_top = true;
            }

            for (0..tiles_per_height) |tile_y| {
                for (0..tiles_per_width) |tile_x| {
                    const abs_tile_x: u32 = @as(u32, @intCast(screen_x)) * tiles_per_width + @as(u32, @intCast(tile_x));
                    const abs_tile_y: u32 = @as(u32, @intCast(screen_y)) * tiles_per_height + @as(u32, @intCast(tile_y));

                    var tile_value: u32 = 1;
                    if (tile_x == 0 and (!door_left or tile_y != (tiles_per_height / 2))) {
                        tile_value = 2;
                    }
                    if (tile_x == (tiles_per_width - 1) and (!door_right or tile_y != (tiles_per_height / 2))) {
                        tile_value = 2;
                    }
                    if (tile_y == 0 and (!door_bottom or tile_x != (tiles_per_width / 2))) {
                        tile_value = 2;
                    }
                    if (tile_y == (tiles_per_height - 1) and (!door_top or tile_x != (tiles_per_width / 2))) {
                        tile_value = 2;
                    }

                    if (tile_x == 10 and tile_y == 6) {
                        if (door_up) {
                            tile_value = 3;
                        }
                        if (door_down) {
                            tile_value = 4;
                        }
                    }

                    tile.setTileValueAbs(&game_state.world_arena, world.tile_map, abs_tile_x, abs_tile_y, abs_tile_z, tile_value);
                }
            }

            door_left = door_right;
            door_bottom = door_top;

            door_right = false;
            door_top = false;

            if (created_z_door) {
                door_down = !door_down;
                door_up = !door_up;
            } else {
                door_up = false;
                door_down = false;
            }

            if (random_choice == 2) {
                if (abs_tile_z == 0) {
                    abs_tile_z = 1;
                } else {
                    abs_tile_z = 0;
                }
            } else if (random_choice == 1) {
                screen_x += 1;
            } else {
                screen_y += 1;
            }
        }

        memory.is_initialized = true;
    }

    const world: *World = game_state.world;
    const tile_map: *tile.TileMap = world.tile_map;

    const tile_side_in_pixels: u32 = 60;
    const meters_to_pixels: f32 = @as(f32, @floatFromInt(tile_side_in_pixels)) / tile_map.tile_side_in_meters;

    for (0..input.controllers.len) |controller_index| {
        const controller: *GameControllerInput = getController(input, controller_index);
        if (controller.is_analog) {
            // NOTE: Analog
        } else {
            // NOTE: Digital
            var delta_player_x: f32 = 0.0;
            var delta_player_y: f32 = 0.0;
            if (controller.button.input.move_up.ended_down) {
                delta_player_y = 1.0;
            }
            if (controller.button.input.move_down.ended_down) {
                delta_player_y = -1.0;
            }
            if (controller.button.input.move_left.ended_down) {
                delta_player_x = -1.0;
            }
            if (controller.button.input.move_right.ended_down) {
                delta_player_x = 1.0;
            }

            delta_player_x *= 10.0;
            delta_player_y *= 10.0;

            var new_player_pos = game_state.player_pos;
            new_player_pos.offset_x += input.dt_for_frame * delta_player_x;
            new_player_pos.offset_y += input.dt_for_frame * delta_player_y;
            new_player_pos = tile.recanonicalizePosition(tile_map, new_player_pos);

            var player_left = new_player_pos;
            player_left.offset_x -= 0.5 * player_width;
            player_left = tile.recanonicalizePosition(tile_map, player_left);

            var player_right = new_player_pos;
            player_right.offset_x += 0.5 * player_width;
            player_right = tile.recanonicalizePosition(tile_map, player_right);

            const point_is_valid = (tile.isTileMapPointEmpty(tile_map, new_player_pos) and
                tile.isTileMapPointEmpty(tile_map, player_left) and
                tile.isTileMapPointEmpty(tile_map, player_right));

            if (point_is_valid) {
                if (!tile.areOnSameTile(&game_state.player_pos, &new_player_pos)) {
                    const new_tile_value = tile.getTileValuePos(tile_map, new_player_pos);

                    if (new_tile_value == 3) {
                        new_player_pos.abs_tile_z += 1;
                    } else if (new_tile_value == 4) {
                        new_player_pos.abs_tile_z -= 1;
                    }
                }
                game_state.player_pos = new_player_pos;
            }
        }
    }

    drawRectangle(buffer, 0.0, 0.0, @floatFromInt(buffer.width), @floatFromInt(buffer.height), 1.0, 0.0, 0.0);

    const screen_center_x = 0.5 * @as(f32, @floatFromInt(buffer.width));
    const screen_center_y = 0.5 * @as(f32, @floatFromInt(buffer.height));

    var rel_row: i32 = -10;
    while (rel_row < 10) : (rel_row += 1) {
        var rel_column: i32 = -20;
        while (rel_column < 20) : (rel_column += 1) {
            const column: u32 = game_state.player_pos.abs_tile_x +% @as(u32, @bitCast(rel_column));
            const row: u32 = game_state.player_pos.abs_tile_y +% @as(u32, @bitCast(rel_row));

            const tile_id = tile.getTileValueAbs(tile_map, column, row, game_state.player_pos.abs_tile_z);

            if (tile_id > 0) {
                var gray: f32 = 0.5;
                if (tile_id == 2) {
                    gray = 1.0;
                }

                if (tile_id > 2) {
                    gray = 0.25;
                }

                if (column == game_state.player_pos.abs_tile_x and row == game_state.player_pos.abs_tile_y) {
                    gray = 0.0;
                }

                const center_x: f32 = screen_center_x - meters_to_pixels * game_state.player_pos.offset_x +
                    @as(f32, @floatFromInt(rel_column)) * @as(f32, @floatFromInt(tile_side_in_pixels));
                const center_y: f32 = screen_center_y + meters_to_pixels * game_state.player_pos.offset_y -
                    @as(f32, @floatFromInt(rel_row)) * @as(f32, @floatFromInt(tile_side_in_pixels));
                const min_x: f32 = center_x - 0.5 * @as(f32, @floatFromInt(tile_side_in_pixels));
                const min_y: f32 = center_y - 0.5 * @as(f32, @floatFromInt(tile_side_in_pixels));
                const max_x: f32 = center_x + 0.5 * @as(f32, @floatFromInt(tile_side_in_pixels));
                const max_y: f32 = center_y + 0.5 * @as(f32, @floatFromInt(tile_side_in_pixels));
                drawRectangle(buffer, min_x, min_y, max_x, max_y, gray, gray, gray);
            }
        }
    }

    const player_r: f32 = 1.0;
    const player_g: f32 = 1.0;
    const player_b: f32 = 0.0;
    const player_left = screen_center_x - 0.5 * meters_to_pixels * player_width;
    const player_top = screen_center_y - meters_to_pixels * player_height;
    drawRectangle(buffer, player_left, player_top, player_left + meters_to_pixels * player_width, player_top + meters_to_pixels * player_height, player_r, player_g, player_b);
}

